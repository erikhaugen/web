package com.livescribe.importsecretkeytool.exception;

public class FailToParseLineException extends Exception {
	private static final long serialVersionUID = 3857844115663568483L;

	public FailToParseLineException() {
	}

	public FailToParseLineException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public FailToParseLineException(String arg0) {
		super(arg0);
	}

	public FailToParseLineException(Throwable arg0) {
		super(arg0);
	}
	
	
}
