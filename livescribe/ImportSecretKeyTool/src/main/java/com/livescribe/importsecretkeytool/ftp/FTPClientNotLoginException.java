package com.livescribe.importsecretkeytool.ftp;

public class FTPClientNotLoginException extends Exception {
	private static final long serialVersionUID = 3857844115663568483L;

	public FTPClientNotLoginException() {
	}

	public FTPClientNotLoginException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public FTPClientNotLoginException(String arg0) {
		super(arg0);
	}

	public FTPClientNotLoginException(Throwable arg0) {
		super(arg0);
	}
	
	
}
