package com.livescribe.datatype.basic;

/**
 * Value Type identifiers
 */
public class Types {
    public static final byte NULL = 0x00;
    public static final byte BOOLEAN = 0x01;
    public static final byte BYTE = 0x02;
    public static final byte SHORT = 0x03;
    public static final byte INT = 0x04;
    public static final byte LONG = 0x05;
    public static final byte UNICODE = 0x06;
    public static final byte BINARY = 0x07;
    public static final byte VALUE_ARRAY = 0x08;
    public static final byte DATA_ARRAY = 0x09;
    public static final byte TERM_ARRAY = 0x0A;
    public static final byte TERMINATOR = 0x0B;
    public static final byte FLOAT = 0x0C;
    public static final byte DOUBLE = 0x0D;
}
